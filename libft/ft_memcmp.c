#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	char unsigned	*str1;
	char unsigned	*str2;
	int				i;

	i = 0;
	str1 = (char unsigned *)s1;
	str2 = (char unsigned *)s2;
	while (n--)
	{
		if (str1[i] != str2[i])
		{
			return (str1[i] - str2[i]);
		}
		i++;
	}
	return (0);
}
