#include "libft.h"

int		ft_strchri(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
		{
			return (i);
		}
		i++;
	}
	if (s[i] == c)
	{
		return (i);
	}
	return (-1);
}
