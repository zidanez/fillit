#include "libft.h"

char		*ft_strnew(size_t size)
{
	char	*str;

	if (!(str = (char *)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	else
	{
		while (size + 1 > 0)
		{
			str[size] = '\0';
			size--;
		}
		str[0] = '\0';
		return (str);
	}
}
